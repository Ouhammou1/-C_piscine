#include <unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			nb *= -1;
			putchar('-');
		}
		if (nb < 10)
		{
			putchar(nb + '0');
		}
		if (nb >= 10)
		{
			ft_putnbr(nb / 10);
			putchar(nb % 10 + '0');
		}
	}
}
