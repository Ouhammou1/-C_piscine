int	ft_sqrt(int nb)
{
	long	i;
	long	x;

	i = 2;
	x = nb;
	if (x <= 0)
	{
		return (0);
	}
	if (x == 1)
	{
		return (1);
	}
	if (x >= 2)
	{
		while (i <= (x / i))
		{
			if (i * i == x)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
